% Рекурсивное вычисление НОД по алгоритму Евклида
gcd(X, 0, X) :- !.  % Базовый случай: НОД(X, 0) = X
gcd(X, Y, Result) :-
    Y > 0,
    Mod is X mod Y,  % Вычисляем остаток от деления
    gcd(Y, Mod, Result).  % Рекурсивный вызов с новыми значениями

% Главный предикат для взаимодействия с пользователем
run :-
    write('ПРОГРАММА ДЛЯ ВЫЧИСЛЕНИЯ НОД'), nl,
    write('Введите первое число: '), read(X),
    write('Введите второе число: '), read(Y),
    (X >= Y -> gcd(X, Y, Result) ; gcd(Y, X, Result)),  % Обеспечиваем порядок X >= Y
    format('НОД чисел ~d и ~d равен ~d', [X, Y, Result]).
